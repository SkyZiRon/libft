#include "libft.h"

int	ft_isalnum(char c)
{
	if ('0' <= c || c <= '9')
		return (1);
	if (('A' <= c && c <= 'Z') || ('a' <= c && c <= 'z'))
		return (1);
	else
		return (0);
}

