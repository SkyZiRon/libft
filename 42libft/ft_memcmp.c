#include <libft.h>

int	*ft_memcmp(void *s1, void *s2, size_t n)
{
	size_t i;
	char *p1;
	char *p2;

	p1 = (char *) s1;
	p2 = (char *) s2;
	i = 0;
	while (i < n)
	{
		if (p1[i] != p2[i])
			return (p1[i] - p2[i]);
		i++;
	}
	return (0);
}